using System;
using System.IO;
using System.Linq;
using System.Reflection;
using BenchmarkDotNet.Loggers;
using BenchmarkDotNet.Toolchains;
using BenchmarkDotNet.Toolchains.CsProj;
using BenchmarkDotNet.Toolchains.DotNetCli;

namespace VirtoCommerce.XCart.Benchmark;

/// <summary>
/// A CsProj toolchain whose generator resolves the benchmark project deterministically by walking up
/// from the benchmark assembly's location, instead of BenchmarkDotNet's default current-directory
/// .sln/.csproj search.
///
/// <para>The default search fails here because the benchmark classes live in this Core <b>library</b>,
/// which sits in a sibling folder of whatever runner exe launched the process. BDN anchors its search
/// on the runner's working directory and only looks in that subtree, so it never finds
/// VirtoCommerce.XCart.Benchmark.Core.csproj and throws "Unable to find ... output exe is different
/// than the name of the .csproj".</para>
///
/// <para>The autogenerated child is always an exe that ProjectReferences the located project, so Core
/// being a library is fine: the child references Core, and the /p:XCartSrc MSBuild property (the
/// baseline-src before/after flag) flows through the child build into Core's XCart source references
/// exactly as it did before the Core/runner split.</para>
/// </summary>
internal static class BenchmarkCoreToolchain
{
    private const string TargetFrameworkMoniker = "net10.0";

    public static IToolchain Instance { get; } = new Toolchain(
        "CoreCsProj",
        new CoreProjectGenerator(TargetFrameworkMoniker, null, null, null),
        new DotNetCliBuilder(TargetFrameworkMoniker),
        new DotNetCliExecutor(null));

    private sealed class CoreProjectGenerator : CsProjGenerator
    {
        public CoreProjectGenerator(string targetFrameworkMoniker, string cliPath, string packagesPath, string runtimeFrameworkVersion)
            : base(targetFrameworkMoniker, cliPath, packagesPath, runtimeFrameworkVersion)
        {
        }

        protected override FileInfo GetProjectFilePath(Type benchmarkTarget, ILogger logger)
        {
            var assembly = benchmarkTarget.GetTypeInfo().Assembly;
            var fileName = $"{assembly.GetName().Name}.csproj";
            var location = assembly.Location;

            // The dll runs from the launching runner's output dir (Core.dll is copied next to the
            // runner exe), so Core.csproj is NOT an ancestor of the dll — it's a sibling project under
            // a shared parent (e.g. tests/). Walk up and, at each ancestor, search the subtree for the
            // project file: the tightest ancestor that contains it (tests/) wins. Independent of the
            // process working directory.
            if (!string.IsNullOrEmpty(location))
            {
                var directory = new DirectoryInfo(Path.GetDirectoryName(location));
                while (directory is not null)
                {
                    FileInfo match = null;
                    try
                    {
                        match = directory.EnumerateFiles(fileName, SearchOption.AllDirectories).FirstOrDefault();
                    }
                    catch (UnauthorizedAccessException)
                    {
                        // Skip directories we can't enumerate; keep walking up.
                    }

                    if (match is not null)
                    {
                        return match;
                    }

                    directory = directory.Parent;
                }
            }

            // Fall back to BDN's current-directory search if the walk-up didn't locate it.
            return base.GetProjectFilePath(benchmarkTarget, logger);
        }
    }
}
